EMOJI_CODES = {}

def getCode(description):
	if len(EMOJI_CODES) == 0:
		for cat in EMOJIS.keys():
			for emoji in EMOJIS[cat]:
				EMOJI_CODES[emoji[1].lower().replace(' ', '_')] = emoji[0]
	if EMOJI_CODES.has_key(description):
		return '&#' + EMOJI_CODES[description] + ';'
	return '[ERROR: Unknown emoji "' + description + '"]'

def getCategoryRepeater(domID):
	items = []
	selected = True
	for category in CATEGORIES:
		items.append({
			'number': EMOJIS[category][0][0],
			'domID': domID,
			'size': '120%',
			'category': category,
			'selected': selected
		})
		selected = False
	return items

def getPaletteCategoryRepeater(domID, category='Faces'):
	items = []
	for emoji in EMOJIS[category]:
		items.append({
			'number': emoji[0],
			'description': emoji[1],
			'domID': domID
		})
	return items

def getPaletteSearchRepeater(domID, search):
	items = []
	for category in CATEGORIES: 
		for emoji in EMOJIS[category]:
			if search.lower() in emoji[1].lower():
				items.append({
					'number': emoji[0],
					'description': emoji[1],
					'domID': domID
				})
	return items